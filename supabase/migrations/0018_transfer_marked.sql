-- ─────────────────────────────────────────────
-- 0018. 사용자 송금 완료 신호
--
-- 직접 계좌이체 모델에서 accepted→paid 전이는 '작가의 입금 확인'이 기준이다.
-- 하지만 사용자가 "송금했다"고 먼저 알릴 수단이 없어 작가가 마냥 기다리게 된다.
-- transfer_marked_at: 사용자가 채팅 송금 카드에서 [송금 완료]를 누른 시각.
--   - 작가에게 "입금 확인해 주세요" 알림을 트리거
--   - 카드 UI에서 버튼 → "확인 대기 중" 상태로 전환
-- 실제 결제 확정(paid)은 여전히 작가 confirmTransfer가 담당(신뢰 시점).
-- ─────────────────────────────────────────────
alter table public.bookings
  add column if not exists transfer_marked_at timestamptz;
